const products = [];

function addProduct (name, price, brand, length, width, profile, weight, flex, meter, description) {

  products.name = name;
  products.price = price;
  products.brand = brand;
  products.length = length;
  products.width = width;
  products.profile = profile;
  products.weight = weight;
  products.meter = meter;
  products.flex = flex;
  products.description = description;
}

